int	ft_str_is_alpha(char *str)
{
	int		i;

	i = -1;
	while (str && str[++i])
		if (str[i] < 'a' || str[i] > 'z')
			if (str[i] > 'Z' || str[i] < 'A')
				return (0);
	return (1);
}
