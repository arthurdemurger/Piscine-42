int	ft_sqrt(int nb)
{
	int		pow;

	pow = 0;
	if (nb < 0)
		return (0);
	while (pow <= 46340)
	{
		if ((pow * pow) == nb)
			return (pow);
		pow++;
	}
	return (0);
}
